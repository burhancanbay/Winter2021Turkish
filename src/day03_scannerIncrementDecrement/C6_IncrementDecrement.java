package day03_scannerIncrementDecrement;

public class C6_IncrementDecrement {

	public static void main(String[] args) {
		int number=10;
		// bir variable in değerini toplayarak artırmak için 3 yöntem kullanabilirim
		// 1.yöntem(acemi yöntemi)
		number=number+5;
	
		// 2.yöntem(usta işi)
		number+=8;
		
		// 3.yöntem sadece bir arttırmak için 
		number++;
		
		// number'ı 6 azaltın
		number-=6;
		
		// number'ı ikiye bölün
		number/=2;
		
		// number'ı üç ile çarpın
		number*=3;
		
		// number'ı bir eksiltin
		number--;
		
	}

}
