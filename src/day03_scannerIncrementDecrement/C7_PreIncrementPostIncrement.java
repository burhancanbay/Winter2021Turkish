package day03_scannerIncrementDecrement;

public class C7_PreIncrementPostIncrement {

	public static void main(String[] args) {
		
		int num=20;
		
		num++; //num=21
		
		System.out.println(num++); // önce yazdýrýyor sonra arttýrýyor
		
		System.out.println(++num);
		System.out.println(num);
		
		

	}

}
